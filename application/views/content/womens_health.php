        <h1>Molecular DNA Testing and Women's Health</h1>
        <p>
            The Human Genome Project has set the stage for countless advancements in our understanding of human DNA and
            its role in the diagnosis of disease. Nowhere is this truer than in the area of women's health.
            Pathologists and clinicians are increasingly turning to the power of molecular genetics
            for early and accurate diagnoses of disease.
            <br/><br/>
            New testing methods are creating opportunities to screen for common disorders affecting women, such as Human
            papillomavirus (HPV), Chlamydia trachomatis (CT), Neisseria gonorrhea (NG), Cystic Fibrosis (CF), Herpes
            Simplex Virus I & II and Inherited Thrombophilia (IHT). These innovations offer pathologists and clinicians
            the ability to add molecular diagnostics to their laboratories.
            <br/><br/>
            <strong><h2>The Advantages of Molecular Diagnostics DNA</h2></strong>
            can be reliably extracted from a variety of sources, including blood, tissue, or cells from a liquid Pap test,
            to produce a sample that can be screened for disease. Using molecular diagnostics, physicians can diagnose
            diseases earlier, or even before they occur. Molecular testing offers superior sensitivity and specificity,
            as well as added diagnostic utility when combined with cytology results.
            <br/><br/>
            <strong><h2>PCR: The Gold Standard for Molecular Testing</h2></strong>
            Polymerase Chain Reaction (PCR) is the most broadly applicable
            method used in gene- based testing. The basis of PCR is the use of an enzyme to amplify specific parts of a gene,
            thereby mimicking what occurs in vivo during DNA synthesis.
            <br/><br/>
            The benefits of PCR include increased flexibility, sensitivity, specificity and reliability.PCR provides
            appropriate clinical sensitivity by amplifying small amounts DNA to pick up minute disease signals,
            allowing for efficient interpretation of DNA mutations. At the same time, PCR's specificity pinpoints a
            particular disease at the exact location of the change in the DNA sequence.
            <br/><br/>
            In just a few hours, PCR-based testing gives pathologists and clinicians the ability to screen for a host of
            disorders efficiently and accurately with superior analytic performance. PCR allows pathologists to
            perform a large number of tests, scalable to the market, and offers proven technology for reimbursable
            clinical laboratory testing.
            <br/><br/>
            <strong><h2>The Benefits of Geno PAP in Women's Health</h2></strong>
            Geno PAP with PCR is more then just a series of tests; it is a strategy for success in women's health.
            Geno PAP allows our pathologist to screen for Human papillomavirus (HPV), Chlamydia trachomatis (CT),
            Neisseria gonorrhea (NG), Cystic Fibrosis (CF), Herpes Simplex Virus I & II and Inherited Thrombophilia
            (IHT) from a single collection.
            <br/><br/>
            Geno PAP identifies 32 HPV types by Roche Cobas 4800. Geno PAP detects an additional seven HPV types
            classified as high-risk when compared to the Hybrid Capture® 2 method, providing better correlation to
            cytology diagnoses and accurate triage management for patients referred on to colposcopy. HPV genotyping
            enables our pathologists to more accurately predict the outcome of HPV infections and to monitor mixed or
            multiple, persistent and re-infections by genotype.

        </p>

