            <h4>Clinical Background</h4>





            NSCLC accounts for about 85% of lung cancers and includes predominantly adenocarcinomas (the most common type

            in the United States) and squamous cell carcinomas. Depending on the patient’s medical status and stage of disease, 

            treatment options include surgery, radiation therapy, and chemotherapy. Although cytotoxic chemotherapy remains 

            an important part of treatment, targeted therapies based on genetic alterations in the tumor are appropriate for selected 

            cases. Identifying mutations in oncogenic associated with non-squamous NSCLC can help determine which patients are 

            more likely to benefit from a targeted therapy. Such oncogenic include EGFR, KRAS, and ALK.
            <br><br>

            <h4>EGFR</h4>

            Activation of the epidermal growth factor receptor (EGFR) protein stimulates protein tyrosine kinase, which leads to 

            activation of signaling pathways associated with cell growth and survival. Both EGFR over expression and activating 

            mutations in the tyrosine kinase domain of the EGFR gene lead to tumor growth and progression. Consequently, 

            EGFR has become a target for anti-cancer drug therapy. Erlotinib and gefitinib are examples of EGFR tyrosine kinase 

            inhibitors (TKIs) that can prevent activation of the signaling pathways and improve response rates in selected NSCLC 

            patients.<br>

            Activating EGFR mutations, which are associated with increased sensitivity to EGFR TKIs, predominate in never-
            smokers, females, and tumors with adenocarcinomas histology. However, these characteristics are not as effective 

            as mutation testing for predicting which patients might benefit from targeted TKIs. The most common mutations 

            associated with sensitivity to EGFR TKIs include exon 19 deletions and the L858R point mutation. 

            associated with response rates of >70% in patients treated with either Erlotinib or gefitinib. Other EGFR mutations (eg, 

            T790M and exon 20 insertion) have been associated with much lower response or acquired resistance to TKIs. 

            <br><br>
            <h4>KRAS</h4>

            The KRAS protein stimulates signaling pathways downstream from EGFR. KRAS mutations lead to a constitutively 

            activated KRAS protein that continually stimulates these downstream pathways. Although EGFR TKIs can block 

            EGFR activation, they cannot block the activity of the mutated KRAS protein. Thus, patients with KRAS mutations 

            tend to be resistant to erlotinib and gefitinib. KRAS mutations are more likely found in adenocarcinomas, in patients 

            who are smokers, and in Caucasian patients rather than East Asians. KRAS mutations are prognostic for poor survival, 

            independent of therapy.

            <br><br>
            <h4>ALK</h4>

            Rearrangements of the gene encoding anaplastic lymphoma kinase (ALK) have been linked to abnormal cell 

            proliferation and NSCLC (most commonly adenocarcinomas). The most common ALK rearrangement in NSCLC 

            is EML4-ALK, which arises from fusion between the 5′ end of the EML4 gene and the 3′ end of the ALK gene on 

            chromosome 2p23. Patients with ALK rearrangements are younger than most patients with NSCLC. EML4-ALK 

            rearrangements are also more common in adenocarcinomas of never or light smokers whose tumors lack EGFR and 

            KRAS mutations. <br>

            Patients with ALK rearrangements do not benefit from EGFR-specific TKI therapy but may be considered for therapy 

            targeting the constitutively activated receptor tyrosine kinase that results from EML4-ALK and other ALK fusions. 

            Crizotinib (Xalkori®,

            metastatic NSCLC in patients whose tumors are positive for ALK as determined using an FDA-approved test. Patients 

            Pfizer) is the first FDA-approved ALK TKI. It is indicated for treatment of locally advanced or 

            with ALK-positive advanced NSCLC have exhibited objective response rates of 50% to 61% in single-arm clinical 

            studies.
            <br><br>

            <h4>Mutation Profiling</h4>

            Although some clinical and histologic features correlate with particular genetic changes, only molecular testing can 

            definitively identify the mutations associated with targeted therapy response or resistance. Additionally, EGFR, KRAS, 

            and ALK mutations are almost always mutually exclusive (i.e., mutations of only 1 of the 3 genes occur within any 

            individual tumor). Therefore, Quest Diagnostics offers a Lung Cancer Mutation Panel that tests for mutations in all 3 

            oncogenic. Individual tests for mutation detection are also available for each gene. 
