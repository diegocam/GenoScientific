            <h4>Interpretive Information</h4>

            Activating EGFR mutations, including exon 19 deletions and the L858R point mutation, predict response to Erlotinib or 

            gefitinib therapy. T790M and exon 20 insertion mutations predict resistance (Table). KRAS mutations predict non-response 

            to EFGR and ALK inhibitors, and thus alternative therapies should be considered. Detection of ALK rearrangements in 

            patients with advanced NSCLC suggests eligibility for treatment with the ALK inhibitor Crizotinib but not with EGFR-
            directed inhibitors.<br><br>

            During therapy, acquisition of additional, secondary mutations can confer resistance to EGFR or ALK inhibitors. Such 

            mutations include the T790M mutation in EGFR, mutations in KRAS, and ALK mutations that may not be detected in this 

            assay (e.g., somatic ALK kinase domain mutations and copy number gains of the fusion gene).



