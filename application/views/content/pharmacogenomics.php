    <h2>Testing places individuals in one of three categories:</h2>
    <br>
    <ul class="normal_ul_list">
        <li>Normal metabolizers (NM) represent the norm for metabolic capacity. In general normal metabolizers can be administered drugs which are substrates of the CYP2C19 enzyme following standard dosing practices. Genotypes consistent with the normal metabolizer phenotype include two active CYP2C19 alleles.</li>
        <br>
        <li>Intermediate metabolizers (IM) may require lower than average drug dose for optimal therapeutic response to medications with the exception of prodrugs. For the majority of drugs consider decreased dosage. For prodrugs, like Plavix, that require activation by CYP2C19, an alternative treatment or increased dose should be considered. Genotypes consistent with the intermediate metabolizer phenotype are those with one active and one inactive CYP2C19 allele.</li>
        <br>
        <li>Poor metabolizers (PM) are at increased risk of drug-induced side effects due to diminished drug elimination or for prodrugs, like Plavix, lack of therapeutic effect resulting from failure to generate the active form of the drug. Alternative treatment should be considered. Genotypes consistent with the poor metabolizer phenotype are those with no active CYP2C19 alleles.</li>
    </ul>
    <br>
    <h2>Co-administration of other drugs</h2>
    Genotype results should be interpreted in context of the individual clinical situation. In all cases monitor for co-administration of CYP2C19 inhibitors which may convert patients to poor metabolizer status. Potential adverse outcomes included overdose toxicity or treatment failure particularly for prodrugs. For more information see GeneMedRx drug-drug and drug-gene interaction software and Cytochrome P450 Metabolism Inhibitor/Inducer Tables. Access GeneMedRx via the patient access code provided at <a href="http://www.GeneMedRx.com/DNAlogin" target="_blank">www.GeneMedRx.com/DNAlogin</a>.
