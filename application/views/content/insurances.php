    <h3>PROFESSIONAL COURTESY POLICY</h3>
    <br>
    <p>
        On October 13, 1994, the Office of Inspector General of the United States
        Department of Health and Human Services issued a Fraud Alert. This Document
        states that the provision of the free laboratory testing for health care provider,
        their families, and their employees is in violation of the Medicare/Medicaid anti-
        kickback statute. Because of the ruling, Genoscientific Is not able to offer or provide
        professional courtesy to our clients.
    </p>
    <br>
    <p>
        Bills from Genoscientific Services are generally for outpatient laboratory testing
        (such as laboratory tests, biopsies and Pap smears) performed at your physician's
        office or a surgery center as well as technical processing of specimens from other
        physicians or hospitals. The slides are prepared in our laboratory and shipped back
        to the referring physician/hospital for interpretation.
    </p>
    <br>
    <p>
        If your physician has requested a laboratory test (such as a laboratory tests,
        biopsy or Pap smear) or consultation, you will receive a billing statement from
        Genoscientific.
    </p>
    <br>
    <p>
        Initially, Genoscientific submits a claim to the insurance carrier identified by your
        physician or hospital. Once your insurance has processed the claim, you should
        receive an Explanation of Benefits from your insurance. This is NOT a bill. You
        do not need to pay anything until you receive a statement from Genoscientific.
        Genoscientific will forward a statement to you showing the application of any
        payments and insurance adjustments. The remaining balance may represent
        your deductible, coinsurance, or the cost of non-covered services, and is your
        responsibility. In all cases, payment is due within 30 days from the statement date.
    </p>
