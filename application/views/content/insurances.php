    <h3>Billing FAQ</h3>
    <br>
    <p>
        Bills from Genoscientific Services are generally for outpatient laboratory testing
        (such as Lab Tests, biopsies and Pap smears) performed at your physician's office or
        a surgery center.
    </p>
    <br>
    <p>
        Bills from Genoscientific Associates, our Laboratory-based pathology practice, are
        for the professional services relating to laboratory testing performed at various area
        hospitals. if you recently had a hospital encounter (either inpatient or outpatient),
        you may very likely receive a bill from Genoscientific as our pathologists direct
        several hospital laboratories.
    </p>
    <br><br>
    <h4>I paid my co-pay at the doctor's office. Why am I getting a bill from Genoscientific?</h4>

    <p>
        Copayments typically cover physician office visits only. Any patient responsibility,
        typically deductible and/or coinsurance, based on your member agreement with
        your health plan will be billed to you once your insurance has processed your claim.
        This will be reflective on your billing statements as well as your Explanation of
        Benefits (EOB) from your insurance.
    </p>

    <br><br>
    <h4>Why have I received a bill when I have insurance that covers my laboratory testing?</h4>

    <p>
        Please remember that insurance plans have varying benefit levels and only you
        can be sure your insurance company processed your claim according to your plan
        provisions. If you feel your claim was processed incorrectly, please contact your
        insurance company.
    </p>

    <br><br>
    <h4>Does Genoscientific accept my insurance?</h4>

    <p>
        Genoscientific is a participating provider with most insurance companies.
    </p>

    <br><br>
    <h4>I have a secondary insurance plan. Does Genoscientific file those claims for me?</h4>

    <p>
        Genoscientific will file all primary and secondary claims on your behalf. Once all
        plans have processed your claim, you will be billed for any remaining portion.
    </p>

    <br><br>
    <h4>I have Medicare or Medicaid. What will I have to pay?</h4>

    <p>
        Medicare patients must meet their annual deductible and a 20% coinsurance. If you
        have a secondary plan, please let us know.<br>
        Medicaid patients will have no out-of-pocket expenses.
    </p>

    <br><br>
    <h4>Can Genoscientific tell me how much my tests will cost?</h4>

    <p>
        It is difficult to estimate the total cost of ordered tests. The pathologist may see the
        need to special laboratory tests, stains or additional testing to be performed in order
        to be able to render a diagnosis.
    </p>

