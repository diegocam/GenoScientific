    <h1>Apolipoprotein E (APOE) 2 Mutations</h1>
    <br>
    <h2>Clinical Implications</h2>
    <br>

    <hr>
    <h3>Implication #1</h3>

    <br>
    <p>
        Individuals who are e2/e2 are prone to develop Type III hyperlipidemia. 
        The E4 allele is associated with increased plasma cholesterol levels and subsequent increased risk for coronary heart disease. 
        ApoE genotype can play a significant role in predicting patient responsiveness to cardiac-related environmental factors, statin therapy, and dietary therapy.

    </p>

    <br><br><br>

    <h3>Implication #2</h3>

    <br>
    <p>
        InApolipoprotein E is present on chylomicrons, VLDLs, and HDLs and functions as a ligand for lipoprotein receptors. 
        The apoE gene has three common alleles (E2, E3, E4) that code for three isoforms of the apoE protein (E2, E3, E4). 
        The alleles are inherited in a co-dominant manner resulting in six genotypes (e2/e2, e2/e3, e2/e4, e3/e3, e3/e4, e4/e4).

    </p>

