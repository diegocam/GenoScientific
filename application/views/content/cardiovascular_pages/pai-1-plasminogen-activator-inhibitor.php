    <h1>
        Plasminogen activator inhibitor (PAI-1)
    </h1>
    <br>

    <hr>
    <h3>
        Plasminogen activator inhibitor (PAI-1), or serpin peptidase

        inhibitor, is an inhibitor of fibrinolysis.
    </h3>

    <br>

    <ul class="normal_ul_list">
        <li>
            PAI-1 inhibits tissue-type (t-PA) and urokinase (uPA)plasminogen activator. PAI-1 stimulates interstitial macrophage

            recruitment and increases transcription of profibrotic genes. PAI-1 is also important for signal transduction, cell adherence, and migration.
        </li>
        <li>
            PAI-1 acts as an acute-phase protein in acute inflammation and is important in the pathogenesis of sepsis. Increased levels of PAI-1are

            correlated with increased severity of disease, increased levels of various cytokines, acute-phase proteins, and coagulation parameters.
        </li>
        <li>
            PAI-1 levels are regulated by metabolic factors, such as triglycerides, cholesterol, and insulin. The single guanosine nucleotide deletion/

            insertion polymorphism (4G/5G) at -675 bp of the SERPINE1 gene is the major genetic determinant of PAI-1 expression. The PAI-1 promoter 4G

            allele, compared to 5G allele, is associated with increased PAI-1 transcription activity, higher PAI-1 plasma levels, and reduced fibrinolysis.
        </li>
    </ul>

    <br><br>


    <h3>Epidemiology</h3>

    <br>

    <ul class="normal_ul_list">
        <li>
            Allele frequency for the 4G allele is 0.52 in Caucasians, 0.38 in
        </li>
        <li>
            Hispanics, and 0.13–0.28 in African-Americans.
        </li>
    </ul>
    <br><br>
    <h3>Genetics</h3>
    <br>
    <ul class="normal_ul_list">
        <li>
            Autosomal dominant inheritance.
        </li>
        <li>
            The 4G/5G insertion/deletion polymorphism is located in the promoter region of the SERPINE1 gene, c.-148-672, on

            chromosome 7q21.3-q22.
        </li>
        <li>
            The molecular mechanism for the 4G allele-mediated higher PAI-1 expression is associated with greater binding of upstream

            stimulatory factor-1 to the E-box adjacent to the 4G site (E-4G) than to the E-5G.
        </li>
        <li>
            Individuals with 4G/5G and 4G/4G genotypes, especially those with other thrombophilic risk factors, are at increased risk for

            venous Thromboembolism (VTE).
        </li>
        <li>
            Both the 4G/5G and 4G/4G genotypes are also associated with increased risk of myocardial infarction (MI).
        </li>
        <li>
            Some studies have associated the 5G/5G genotype with elevated risk of ischemic stroke (IS), along with decreased PAI-1 transcription in brain

            astrocytes and lower PAI-1 activity levels compared to the 4G/4G genotype.
        </li>
    </ul>
    <br><br>
    <h3>Indications for Ordering</h3>
    <br>
    <ul class="normal_ul_list">
        <li>
            To assess genetic susceptibility for VTE or MI in individuals with a personal or family history of thrombotic events.
        </li>
        <li>
            Risk-benefit assessment for preventive or therapeutic interventions for VTE or MI.
        </li>
    </ul>
    <br><br>
    <h3>Contraindication</h3>
    <br>
    <ul class="normal_ul_list">
        <li>Fetal testing.</li>
    </ul>
    <br><br>
    <h3>Interpretation</h3>
    <br>
    <ul class="normal_ul_list">
        <li>5G/5G genotype: Two copies of the PAI-1 5G allele were detected.</li>
        <li>4G/5G genotype: One copy of the 4G allele was detected; this result is associated with an increased risk for VTE and MI.</li>
        <li>4G/4G genotype: Two copies of the 4G allele were detected; this result is associated with an increased risk for VTE and MI.</li>
        <li>Results of 4G/5G PAI-1 genotyping should be interpreted in the context of other laboratory tests and clinical information.

            Thrombotic risk may be altered by other genetic and non-genetic factors not assessed by this assay.</li>
    </ul>
    <br><br>
    <h3>Methodology</h3>
    <br>
    <ul class="normal_ul_list">
        <li>
            Polymerase chain reaction (PCR) and fluorescence monitoring to detect the PAI-1 4G/5G genotype, c.-148-672.
        </li>
        <li>
            Analytical sensitivity and specificity are 99 percent.
        </li>
    </ul>
    <br><br>
    <h3>Limitations</h3>
    <br>
    <ul class="normal_ul_list">
        <li>
            Variants in the SERPINE1 gene, other than the 4G/5G, are not evaluated.
        </li>
        <li>
            Rare diagnostic errors may occur due to primer-site mutations.
        </li>
    </ul>

