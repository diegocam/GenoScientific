    <h1>Factor V Leiden (F5) R506Q Mutation</h1>
    <br>
    <h2>TO DETERMINE THE GENETIC CONTRIBUTION TO VENOUS THROMBOSIS IN AFFECTED INDIVIDUALS OR THOSE WITH A 

        SIGNIFICANT FAMILY HISTORY</h2>
    <br>
    <hr>
    <br>
    <h3>Clinical Background</h3>
            <br>
            <ul class="normal_ul_list">
                <li>Factor V Leiden (FVL) is a variant of the factor V protein having prolonged activity due to activated 

                    protein C (APC) resistance.</li>
                <li>APC limits clot formation by proteolytic inactivation of the coagulation factors factor Va and VIIIa. The 

                    genetic variation (R506Q) responsible for FVL prevents a peptide bond in the molecule from being 

                    cleaved.</li>
                <li>Resistance to APC activity increases the risk of deep-vein thrombosis (DVT) and recurrent second- or 

                    third-trimester pregnancy loss.</li>
                <li>The FVL mutation is the most common genetic risk factor for thrombosis and accounts for >90 percent 

                    of APC resistance.</li>


            </ul>
            <br>
            The expression of FVL thrombophilia is impacted by:
            <br>
            <ul class="normal_ul_list">
                <li>Coexisting genetic thrombophilic disorders (e.g., factor II G20210A mutation, protein C deficiency, or 

                    homocystinemia).</li>
                <li>Acquired thrombophilic disorders (e.g., malignancy, hyperhomocysteinemia, high factor VIII levels).</li>
                <li>Non-genetic risk factors (e.g., pregnancy, oral contraceptive use, hormone-replacement therapy, 

                    selective estrogen-receptor modulators, travel, immobilization, central venous catheters, surgery, 

                    transplantation and advanced age).</li>
            </ul>
            <br><br>


            <h3>Epidemiology</h3>
            <br>

            Approximately 5 percent of Caucasians, 2 percent of Hispanics, 1 percent of African-Americans and Native 

            Americans, and 0.5 percent of Asians are heterozygous.

            Homozygous FVL occurs in about one in 5,000 individuals.
            <br><br><br>


            <h3>Genetics: Autosomal dominant inheritance</h3>
            <br>
            <ul class="normal_ul_list">
                <li>FVL results from a G>A substitution at nucleotide position 1,691 in the F5 gene.</li>
                <li>FVL is inherited in an autosomal dominant manner, and de novo mutations are rare.</li>
                <li>FVL heterozygotes have a five- to 10-fold increased risk for thrombosis.</li>
                <li>FVL homozygotes have a 50- to 100-fold increased risk for thrombosis.</li>
                <li>The lifetime risk for DVT is 80 percent for FVL homozygotes and 12–20 percent for heterozygotes.</li>
            </ul>
            <br><br>

            <h3>Indications for Ordering</h3>
            <br>
            <ul class="normal_ul_list">
                <li>Diagnostic testing for individuals with a personal history of thrombosis.</li>
                <li>To determine a genetic cause for recurrent second- or third trimester pregnancy loss.</li>
                <li>Presymptomatic evaluation of individuals with a family history of thrombosis or a family member identified 

                    to have FVL. To assess genetic susceptibility for VTE, MI, or CAD in Caucasians with a personal or family 

                    history of thrombotic events.</li>
                <li>Risk-benefit assessment for preventive or therapeutic interventions for VTE, MI, or CAD in Caucasians.</li>
            </ul>
            <br><br>

            <h3>Interpretation</h3>
            <br>

            <ul class="normal_ul_list">
                <li>Homozygosity for the F5 R506Q mutation is associated with APC resistance and an increased risk for 

                    venous thrombosis.</li>
                <li>Heterozygosity for the F5 R506Q mutation is associated with APC resistance and an increased risk for 

                    venous thrombosis.</li>
                <li>Results of F5 genotyping can be accurately determined for patients on oral anti-coagulant and standard 

                    heparin therapy. </li>

            </ul>
            <br><br>

            <h3>Limitations</h3>
            <br>
            <ul class="normal_ul_list">
                <li>F5 gene mutations, other than R506Q, are not evaluated by this assay.</li>
                <li>Rare diagnostic errors may occur due to primer-site</li>
            </ul>

            <br><br>

            <h3>Methodology</h3>
            <br>

            <ul class="normal_ul_list">
                <li>Polymerase chain reaction and fluorescent monitoring to detect the F5 R506Q (1691G>A) mutation. <br>

                    Note: Standardized nomenclature for the FVL mutation is c.1601G>A(p.Arg534Gln).</li>
                <li>Analytical sensitivity and specificity are 99.9 percent.</li>
            </ul>

