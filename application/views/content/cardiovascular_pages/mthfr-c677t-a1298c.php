    <h3>Genetics</h3>
    <br>
    <ul class="normal_ul_list">
        <li>Identification of genetic etiology for persistent hyperhomocysteinemia</li>
        <li>Assess risk of venous thrombosis. With other genetic thrombophilic factors (factor V Leiden or Prothrombin G20210A), detection of MTHFR mutations indicates 

    significantly increased risk for venous thrombosis cannot be excluded.</li>
    </ul>
    <br><br>
    <h3>Indications for Ordering</h3>
    <br>
    The MTHFR mutations result in elevated plasma homocysteine levels which are a strong and independent risk factor for occlusive vascular disease.
    <br>

    Testing for MTHFR Genotyping is recommended for:
    <br>
    <ul class="normal_ul_list">
        <li>Patients with elevated total plasma homocysteine levels.</li>
        <li>Patients with positive family history of arterial thrombosis.</li>
        <li>Patients with documented atherosclerotic (coronary artery, cerebrovascular, or periperal vascular) disease.</li>
    </ul>
    <br>
    Plasma homocysteine levels may be normalized by nutritional supplements of folic acid and vitamin B12.
    <br><br><br>
    <h3>Contraindications</h3>
    <br>
    <ul class="normal_ul_list">
        <li>General population screening</li>
        <li>Fetal testing</li>
    </ul>
    <br><br>
    <h3>Interpretation</h3>
    <br>
    Hyperhomocysteinemia may be due to inherited mutation in the MTHFR gene, or may be secondary to folic acid and/or vitamin B12 deficiency. Patients homozygous for the C677T mutation have a 60 - 65% reduction in 

    MTHFR activity and twice the plasma homocysteine concentration compared to normal individuals with no mutation. A second MTHFR gene mutation, A1298C is significant in cases of compound homozygosity with the 

    C677T mutation.<br>

    Analytical sensitivity and specificity are 99.9 percent.

