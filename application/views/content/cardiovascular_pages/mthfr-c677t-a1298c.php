    <h3>Clinical Background</h3>
    <br>
    <span class="bold_font">Clinical Significance:</span>
    <ul class="normal_ul_list">
        <li>Hyperhomocysteinemia is a widely recognized risk factor for coronary artery disease, venous thrombosis, and stroke. It is also involved in the pathogenesis of neural tube defects, 

            stillbirths, and recurrent pregnancy loss. Methylenetetrahydrofolate reductase (MTHFR) is a key enzyme in the metabolism of homocysteine. This enzyme catalyzes the reduction of 

            5, 10-methylenetetrahydrofolate to 5-methyltetrahydrofolate, which is the major circulating form of folate. Folate is a cofactor in re-methylation of homocysteine. MTHFR gene can be 

            silenced by point mutations. This can result in low folate and increased plasma levels of homocysteine.</li>
        <li>C677T is commonly seen in Caucasians of European descent. The frequency of homozygotes for this mutation ranges from <5 to 20 percent based on population studied. Based on 

            limited data, the frequency of A1298C homozygotes is estimated to be 9% and the frequency of C677T/A1298C heterozygotes is estimated to be 15 to 20 percent. Both C677T and 

            A1298C alleles are found to decrease MTHFR enzyme activity.</li>
        <li>Only C677T homozygotes and 677T/A1298C heterozygotes are associated with an increase in plasma homocysteine levels.</li>
    </ul>

    <br>
    <span class="bold_font">Reference Range:</span>
    <br>
