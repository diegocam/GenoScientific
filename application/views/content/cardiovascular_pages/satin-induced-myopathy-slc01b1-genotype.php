    <h3>The science behind the Statin Induced Myopathy (SLCO1B1) Genotype</h3>

    <br>

    <p>
        Despite their proven efficacy, studies estimate that 25—50% of patients with CVD stop taking their statin medications as directed. Many do so because

        of statin induced myopathy—the onset of muscle aches, spasms, and pain associated with statin therapy. This condition can be extremely unpleasant 

        and, for some, completely debilitating.
        <br><br>
        An association has been established between statin induced myopathy and variations in the SLCO1B1 gene.
        <br><br>
        The SLCO1B1 gene encodes for the organic anion transporting polypeptide 1B1 (OATP1B1), an influx transporter produced in the liver that mediates

        the hepatic uptake and metabolism of statins.
        <br><br>
        Inherited variations in the SLCO1B1 gene known as SNPs (single nucleotide polymorphisms) affect the function of this transporter.1

        this variant, especially in homozygotes, results in significantly decreased ability to take up statins, less effectiveness of the statin in lowering LDL-C, 

        higher blood levels after dosing, and an increased risk of myopathy. Studies show that people who have particular inherited variations on the SLCO1B1 

        gene are four- to 10-times more likely to suffer myopathy as a side effect.

    </p>
    <br><br>
    <h3>SLCO1B1 Genotype Predicts Ability to Metabolize Statins</h3>
    <br>
    Three SLCO1B1 genotypes have been identified and classified in terms of their effect on statin metabolism in the liver—normal (T/T), heterozygote (T/

    C), and homozygote (C/C):
    <br>
    <br>
    <ul class="normal_ul_list">
        <li>
            The T/T genotype (valine/valine) is classified as normal. These patients have a normal ability to metabolize statins (about 70% of the

            population). Standard doses of statins are recommended for LDL-C lowering and CVD risk reduction.
        </li>
        <li>
            The T/C genotype (valine/alanine) is classified as an intermediate metabolizer. These patients have a decreased ability to metabolize

            statins (about 26% of the population). They are at a four-fold increased risk for developing statin induced myopathy. They also achieve less LDL-C 

            lowering from the statin that they receive.
        </li>
        <li>
            The C/C genotype (alanine/alanine) is classified as a poor metabolizer. These patients have a significantly decreased ability to metabolize

            statins (up to 5% of the population). They are at a seventeen-fold increased risk of developing myopathy on statin therapy.
        </li>
    </ul>

