    <h1>Factor II (F2) G20210A Mutation</h1>
    <br>
    <h2>ASSESS GENETIC RISK FOR THROMBOSIS ASSOCIATED WITH INCREASED PLASMA PROTHROMBIN</h2>
    <br>

    <hr>
    <h3>Clinical Background</h3>
    <br>



    <span class="bold_font">Detection and genotyping of a single point mutation of the human Factor II gene</span>
    <br>
    An in vitro diagnostic test that allows the detection and genotyping of a single point mutation (G to A at position 20210) 

    of the human Factor II gene from DNA isolated from human whole peripheral blood. This test is performed on the 

    LightCycler® Instrument utilizing polymerase chain reaction (PCR) for the amplification of Factor II DNA recovered from 

    clinical samples and fluorigenic target-specific hybridization for the detection and genotyping of the amplified Factor II 

    DNA.

    <br><br><br>

    <h3>Genetics</h3>
    <br>
    <ul class="normal_ul_list">
        <li>Variable Penetrance; many adult patients who are heterozygous or homozygous for G20210A do not experience VTE.</li>
        <li>Adult heterozygotes for G20210A have a two to fourfold increase in thrombotic risk.</li>
        <li>Homozygosity for G20210A is rare, but it increases thrombotic risk above that seen in G20210A heterozygotes.</li>
        <li>
            Patients heterozygous for F2 G20210A who also carry factor V Leiden R506Q have an earlier age of VTE and a higher risk of recurrent thrombosis than heterozygotes for either single gene mutation. Coinheritance of both mutations occurs in 
            approximately one in 1,000 individuals in the general population and in 1–5 percent of individuals with VTE.
        </li>
    </ul>

    <br><br>

    <h3>Indications for Ordering</h3>
    <br>
    <ul class="normal_ul_list">
        <li>Diagnostic testing for patients with VTE (especially before age 50) or an unprovoked VTE at any age.</li>
        <li>Women with VTE associated with pregnancy, use of oral contraceptives, or HRT.</li>
        <li>Women with unexplained pregnancy loss after the first trimester.</li>
        <li>Variable Penetrance; many adult patients who are heterozygous or homozygous for G20210A do not experience VTE.</li>
        <li>Adult heterozygotes for G20210A have a two to fourfold increase in thrombotic risk.</li>
