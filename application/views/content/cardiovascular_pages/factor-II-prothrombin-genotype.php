        </li>
    </ul>

    <br><br>

    <h3>Interpretation</h3>
    <br>
    <ul class="normal_ul_list">
        <li>Detection of one or two copies of the Prothrombin G20210Amutation is associated with increased Prothrombin levels and 

            risk for venous thrombosis.</li>
        
        <li>If the Prothrombin mutation is identified, testing for other inherited or acquired thrombophilic disorders, including DNA 

    testing for the factor V Leiden mutation, measurement of total plasma homocysteine concentration, serological assays for 

    anticardiolipin antibodies, and multiple phospholipid dependent</li>
        <li>Coagulation assays for lupus inhibitor, is recommended.</li>
        <li>If the Prothrombin G20210A mutation is not detected, this does not exclude elevated Prothrombin levels and hereditary forms 

    of venous thrombosis due to other causes.</li>

    </ul>

    <br>
    Analytical sensitivity and specificity are 99.9 percent.

