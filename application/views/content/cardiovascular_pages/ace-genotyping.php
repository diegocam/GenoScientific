    <h1>ACE Genotyping for Coronary Risk Assessment</h1>
    <br>
    <h2>Angiotensin I Converting Enzyme Polymorphism Analysis, ACE Genotyping</h2>
    <br>

    <hr>
    <h3>Indications for Ordering</h3>

    <br>
    <p>
        This test assists in the risk assessment for coronary artery disease. Genetic predisposition for cardiovascular disease

        appears to be the end result of cumulative effects of multiple genetic polymorphisms, which would confer only modestly

        increased risk when present as a single genetic risk factor. Therefore, testing for multiple genotypes with known synergistic

        effects, including ACE, glycoprotein Pl A1/A2, MTHFR (hyper-homocysteinemia), AGT (angiotensinogen), and AT1 

        (Angiotensin II receptor type 1) is suggested.
    </p>

    <br>
    <p>
        Testing for ACE Genotyping is recommended for:
    </p>
    <br>

    <ul class="normal_ul_list">
        <li>
            Risk assessment for coronary artery disease and essential hypertension in patients with positive
            family history.
        </li>
        <li>
            Assessment of risk for recurrent coronary events and coronary restenosis after stent implantation.
        </li>
        <li>
            Therapy management (ACE inhibitors, angiotensin receptor inhibitors) in patients with
            hypertension and positivity for ACE, AGT, and AT1 gene polymorphisms
        </li>
    </ul>

    <br><br>

    <h3>Interpretation</h3>

    <br>
    <ul class="normal_ul_list">
        <li>
            The test detects the Insertion/Deletion (I/D) polymorphism in the angiotensin-converting enzyme (ACE) gene.
        </li>
        <li>
            All DD genotypes will be confirmed with a second PCR procedure that is specific for the D allele.
        </li>
        <li>
            The DD genotype is associated with elevated plasma ACE levels (twice higher than II genotype).
        </li>
        <li>
            ID heterozygotes have intermediate ACE plasma levels. 
        </li>
        <li>
            The DD genotype is associated with increased risk for coronary artery disease, myocardial infarction, coronary

            restenosis after stent implantation and thrombotic events post-surgery.
        </li>
    </ul>
