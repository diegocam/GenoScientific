    <h1>
        Interleukin-6 (IL-6) G-174C Polymorphism
    </h1>
    <hr>
    <h3>
        General Information
    </h3>

    <br>

    <p>
        IL6 (Interleukin 6, OMIM*147620) is a highly controlled cytokine and is one of the 

        most important regulators of the inflammatory process. IL6 stimulates the immune 

        response and is a key factor in the formation of acquired immunity. The IL6 G-174C 

        polymorphism, where in general the -174C variant is associated with lower IL6 

        secretion, is involved in the development and the course of several diseases, e.g. 

        Kaposi sarcoma (statistically overrepresented: -174G), insulin-dependent diabetes 

        mellitus4 (risk variant: -174C), Crohndisease (growth retarding: -174G).
    </p>
    <br>
    <p>
        Interleukin-6 (IL-6) is synthesized in response to diverse inflammatory stimuli and

        causes cellular damage and promotes atherosclerotic process. Elevated

        concentrations of IL-6 are associated with the development and severity of coronary 

        heart disease (CHD), as well as with the transition to plaque instability and 

        subsequent poor outcome. Concurrently, there is the presence of chronic gastric, 

        lung, and gum inflammation. A G174C mutation in IL-6 gene is associated with :   
    </p>
    <br>
    <br>
    <ul class="normal_ul_list">
        <li>
            Increased plasma IL-6 levels after coronary artery bypass surgery.
        </li>
        <li>
            Predicts poor prognosis in patients with abdominal aortic aneurysms.
        </li>
        <li>
            CC homozygotes demonstrate a more than 2-fold reduction in risk of CHD

            (odds ratio 0.46) after treatment with statin drugs (such as Pravastatin) 

            compared to the GG and GC genotypes;
        </li>
        <li>
            Involved in the pathogenesis of hyperandrogenism (which is a known risk

            factor for coronary artery disease). For these reasons the IL-6 genotype may 

            provide a more robust marker of cardiovascular risk.
        </li>
    </ul>

    <br><br>


    <h3>Indications for Testing for IL6 G-174C Polymorphism</h3>

    <br>
    <ul class="normal_ul_list">
        <li>
            Increased plasma IL-6 levels after coronary artery bypass surgery
        </li>
        <li>
            Predicts poor prognosis in patients with abdominal aortic aneurysms
        </li>
        <li>
            CC homozygotes demonstrate a more than 2-fold reduction in risk of CHD (odds ratio 0.46) after 

            treatment with statin drugs (such as Pravastatin) compared to the GG and GC genotypes
        </li>
        <li>
            Involved in the pathogenesis of hyperandrogenism (which is a known risk factor for coronary 

            artery disease). For these reasons the IL-6 genotype may provide a more robust marker of 

            cardiovascular risk.
        </li>
        <li>
            IL-6 G-174C has been confirmed to be an important risk biomarker for some diseases such as stroke, cancer, 

            mental retardation and ankylosing spondylitis.
        </li>
        <li>
            Assess genetic susceptibility for venous Thromboembolism (VTE) or myocardial infarction (MI) in 

            individuals with a personal or family history of thrombotic events 
        </li>
        <li>
            Risk-benefit assessment for preventive or therapeutic interventions for VTE or MI
        </li>
    </ul>

