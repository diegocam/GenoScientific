    <h1>
        Factor XIII (F13A1) V34L Variant
    </h1>
    <br>
    <h2>To help assess thrombotic risk in Caucasians</h2>
    <hr>
    <h3>
        Clinical Background
    </h3>

    <br>

    <ul class="normal_ul_list">
        <li>
            Factor XIII (FXIII), or fibrin stabilizing factor, is an enzyme that cross-links fibrin during clot formation.
        </li>
        <li>
            FXIII circulates in the plasma as tetramer of two catalytic A subunits and two carrier B subunits.
        </li>
        <li>
            FXIII is activated by thrombin and calcium into factor XIIIa, which is involved in wound repair, cytoskeletal

            remodeling, phagocytosis, placental attachment, and inflammatory processes.
        </li>
        <li>
            The F13A1 gene encodes the FXIII A subunit. A common F13A1 sequence variant, V34L, increases the rate

            of FXIII activation by thrombin, resulting in prematurely depleted FXIIIa.
        </li>
        <li>
            V34L also affects the structure of the cross-linked fibrin clot. At high fibrinogen concentrations, fibrin clots of

            V34L carriers have looser structure and thicker fibers and are degraded faster by fibrinolysis, thus offering 

            protection against thrombotic events.
        </li>
    </ul>

    <br><br>


    <h3>Epidemiology</h3>

    <br>

    <p>
        Allele frequency for the V34L sequence variant is 0.27 in Caucasians, 0.17 in Africans, 0.01 in Asians, and

        0.29 in American Indians.
    </p>
    <br><br>
    <h3>Genetics: Autosomal dominant inheritance</h3>
    <ul class="normal_ul_list">
        <li>
            The V34L sequence variant results from a G>T substitution at position 103 in the F13A1 gene, which is

            located on chromosome 6p24-25.
        </li>
        <li>
            In Caucasians, V34L is associated with a reduced risk for pulmonary embolism and deep-vein thrombosis, a

            modest reduction in risk for MI, and a slight protective effect against CAD.
        </li>
        <li>
            A single study suggests V34L may also be associated with idiopathic spontaneous subconjunctival

            hemorrhage (SSH); however, this finding has not been confirmed.
        </li>
        <li>
            Gene-environment and gene-gene interactions may influence the protective effect of V34L:
        </li>
        <li>
            In a UK Asian population, insulin resistance negated the protective effect of V34L.
        </li>
        <li>
            A variant allele (4G/4G) of the plasminogen activator inhibitor I gene (PAI-1) may also reduce the protective

            effect of V34L 3
        </li>
    </ul>

    <br><br>
    <h3>Indications for Ordering</h3>
    <ul class="normal_ul_list">
        <li>
            To assess genetic susceptibility for VTE, MI, or CAD in Caucasians with a personal or family history of

            thrombotic events.
        </li>
        <li>
            Risk-benefit assessment for preventive or therapeutic interventions for VTE, MI, or CAD in Caucasians.
        </li>
    </ul>
    <br><br>
    <h3>Indications for Ordering</h3>
    <ul class="normal_ul_list">
        <li>
            Negative: The F13A1 V34L sequence variant was not detected.
        </li>
        <li>
            Heterozygous: One copy of the V34L sequence variant was detected; this is associated with a

            reduced risk for VTE, MI, and CAD in Caucasian individuals.
        </li>
        <li>
            Homozygous: Two copies of the V34L sequence variant were detected; this is associated with a

            reduced risk for VTE, MI, and CAD in Caucasian individuals.
        </li>
        <li>
            Results of V34L genotyping should be interpreted in the context of other laboratory tests and

            clinical information. The protective effect of the V34L variant has only been established in

            Caucasian populations and may be altered by other genetic and non-genetic factors not

            assessed by this assay.
        </li>
        <li>
            The presence of the V34L variant in the sample is associated with decreased/reduced risk for

VTE, MI, and CAD.
        </li>
    </ul>
    
